package com.knowledge.questioncard.config;

import com.knowledge.questioncard.util.JwtUtil;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.stereotype.Component;
import org.springframework.web.servlet.HandlerInterceptor;

@Slf4j
@Component
@RequiredArgsConstructor
public class JwtInterceptor implements HandlerInterceptor {
    
    private final JwtUtil jwtUtil;
    private final JwtConfig jwtConfig;
    
    @Override
    public boolean preHandle(HttpServletRequest request, HttpServletResponse response, Object handler) {
        // 记录请求路径用于调试
        String requestURI = request.getRequestURI();
        String contextPath = request.getContextPath();
        String servletPath = request.getServletPath();
        log.info("JwtInterceptor拦截到请求: {} {}, contextPath={}, servletPath={}", request.getMethod(), requestURI, contextPath, servletPath);
        
        // OPTIONS请求直接放行
        if ("OPTIONS".equals(request.getMethod())) {
            return true;
        }
        
        String token = null;
        
        // 1. 优先从Header获取token
        String authHeader = request.getHeader(jwtConfig.getHeaderName());
        if (authHeader != null && authHeader.startsWith(jwtConfig.getTokenPrefix())) {
            token = authHeader.substring(jwtConfig.getTokenPrefix().length()).trim();
        }
        
        // 2. 如果Header中没有,尝试从URL参数获取(用于SSE等无法设置Header的场景)
        if (token == null || token.isEmpty()) {
            token = request.getParameter("token");
            if (token != null) {
                token = token.trim();
            }
        }
        
        // 3. 检查是否手动传递了userId参数
        String manualUserId = request.getParameter("userId");
        if (manualUserId != null && !manualUserId.isEmpty()) {
            try {
                Long userId = Long.parseLong(manualUserId);
                log.info("手动传递userId: {}", userId);
                request.setAttribute("userId", userId);
                // 可以设置默认的username和role
                request.setAttribute("username", "manual_user_");
                request.setAttribute("role", "USER");
                return true;
            } catch (NumberFormatException e) {
                log.warn("手动传递的userId格式无效: {}", manualUserId);
            }
        }

        // 4. 验证token
        if (token != null && !token.isEmpty() && jwtUtil.validateToken(token)) {
            // 将用户信息存入request attributes
            request.setAttribute("userId", jwtUtil.getUserIdFromToken(token));
            request.setAttribute("username", jwtUtil.getUsernameFromToken(token));
            request.setAttribute("role", jwtUtil.getRoleFromToken(token));
            return true;
        }
        
        // 未认证或token无效
        log.warn("JWT验证失败 - 请求路径: {}, Token: {}", requestURI, token != null ? "存在但无效" : "不存在");
        response.setStatus(HttpServletResponse.SC_UNAUTHORIZED);
        return false;
    }
}